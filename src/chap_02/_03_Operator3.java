package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        // 참이면 true, 거짓이면 false
        System.out.println(5 > 3);
        System.out.println(5 < 3);
        System.out.println(5 == 5);
        System.out.println(5 <= 7);
        System.out.println(10 >= 7);
        System.out.println(1 != 2);
        System.out.println(10 != 10);
    }
}
